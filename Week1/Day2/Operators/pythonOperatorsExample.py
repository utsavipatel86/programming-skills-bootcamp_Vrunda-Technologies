x=100
y=3
#arithmetic operators
print("Addition ",x+y)
print("Subtraction ",x-y)
print("Multiplication ",x*y)
print("Division ",x/y)
print("Floor Division ",x//y)
print("Modulus ",x%y)
print("Power ",x**2)

#relational operators
print("Less than ",x<y)
print("Greater than ",x>y)
print("Equals ",x==y)
print("Less or equals ",x<=y)
print("Greater or equals ",x>=y)
print("Not Equals",x!=y)

#logical operators
print("Use of 'and' operator : ",x>y and x==100)#answer should be True
print("Use of 'and' operator : ",x>y and x!=100)#answer should be False
print("Use of 'or' operator : ",x>y or x==100)#answer should be True
print("Use of 'or' operator : ",x>y or x!=100)#answer should be True